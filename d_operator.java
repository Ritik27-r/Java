public class d_operator {
public static void main(String[] args) {
        // Arithmetic operator-> +,-,*,/,%,++,--
        System.out.println(2 + 3);
        System.out.println(2 - 3);

        // Assignment operator-> =,+=,-=,*=,/=,%=,&=,|=,^=,>>=,<<=
        int x = 5;// value is assigned using assignment operator

        // Comparison operator-> ==,!=,>,<,<=,>=
        System.out.println(3 < 5);

        //Logical operator-> &&,||,!
        System.out.println(x < 5 &&  x < 10);
        System.out.println(x < 5 || x < 4);
        System.out.println(!(x < 5 && x < 10));

    }
}
